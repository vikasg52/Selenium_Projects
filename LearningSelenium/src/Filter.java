import java.util.List;
import java.util.stream.Collectors;
import org.openqa.selenium.By;
import org.openqa.selenium.WebDriver;
import org.openqa.selenium.WebElement;
import org.openqa.selenium.chrome.ChromeDriver;
import org.testng.Assert;

public class Filter {

	public static void main(String[] args) {
		System.setProperty("webdriver.chrome.driver", "D:\\chromedriver_win32\\chromedriver.exe");
		WebDriver driver = new ChromeDriver();
		driver.manage().window().maximize();
		driver.get("https://rahulshettyacademy.com/seleniumPractise/#/offers");
		driver.findElement(By.id("search-field")).sendKeys("Rice");
		List<WebElement> vegetables = driver.findElements(By.id("//tr/td[1]"));
		List<WebElement> filterList = vegetables.stream().filter(vegetable -> vegetable.getText().contains("Rice"))
				.collect(Collectors.toList());
		Assert.assertEquals(vegetables.size(), filterList.size());
	}

}
